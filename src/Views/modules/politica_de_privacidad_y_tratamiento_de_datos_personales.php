						<b>2. Justificación legal</b>
					</button>
				</h2>
				<div id="flush-collapseTwo" class="accordion-collapse collapse" aria-labelledby="flush-headingTwo" data-bs-parent="#accordionFlushExample">
					<div class="accordion-body">
						<p class="text-justify">•  Articulo 15 de la Constitución Política de Colombia establece que cualquier persona
							tiene derecho a conocer, actualizar y rectificar los datos personales que existan
							sobre ella en banco de datos o archivos de entidades públicas o privadas y ordena a
							quienes tengan datos personales de terceros, respetar los derechos y garantías
							previstos en la Constitución cuando se recolecta, trata y circula esa clase de
						información.</p>
						<p class="text-justify">•  Ley Estatutaria 1266 de 2008, por medio de la cual se dictan las disposiciones
							generales del hábeas data y se regula el manejo de la información contenida en
							bases de datos personales, en especial la financiera, crediticia, comercial, de
						servicios y la proveniente de terceros países y se dictan otras disposiciones.</p>
						<p class="text-justify">•  Ley 1273 de 2009, por medio de la cual modifica el Código Penal, se crea un nuevo
							bien jurídico tutelado - denominado “de la protección de la información y de los
							datos”- y se preservan integralmente los sistemas que utilicen las tecnologías de la
						información y las comunicaciones, entre otras disposiciones.</p>
						<p class="text-justify">•  Ley Estatutaria 1581 de 2012, por medio de la cual dictan disposiciones generales
						para la protección de datos personales.</p>
						<p class="text-justify">•  Decreto 1377 de 2013, por medio de la cual se reglamenta parcialmente la Ley 1581
						de 2012.</p>
						<p class="text-justify">•  Decreto 1074 de 2015, capitulo 25, el cual reglamenta parcialmente la Ley 1581 de 2012.</p>
