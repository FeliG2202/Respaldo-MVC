						<p class="text-justify">Establecer los lineamientos y políticas para el Tratamiento de Datos Personales, para,
							recolectar, usar, circular, transferir y ceder los datos personales de trabajadores,
							proveedores, clientes, pacientes, acreedores y en general todas las personas naturales y/o
							jurídicas con las que tiene relación por efecto de la ejecución de actividades de prestación
							de servicios de salud. Teniendo en cuenta el servicio que se presta existe tanto para las
							empresas como para sus trabajadores el deber de guardar reserva y discreción sobre los
							datos de sus pacientes o sobre aquellos relacionados con la situación propia de los
							mismos, que llegaren a conocer en desarrollo de sus actividades. Es pertinente indicar que
							en el ejercicio de las funciones legales que el Estado ha delegado para garantizar la
							prestación del servicio de salud, se recaban datos de salud y así mismo, existe el deber
							legal de mantener bajo reserva la historia clínica de los pacientes de acuerdo con lo
							establecido en la Ley 23 de 1981, la Resolución 1995 de 1999 y las bases de datos del
							sistema general de seguridad social reguladas por la Resolución 1344 de 2012, Decreto
						reglamentario 3380 de 1981, Decreto 1570 de 1993.</p>
						<p class="text-justify">JUNICAL MEDICAL S.A.S trata datos personales dentro de los que se encuentran datos
							sensibles, datos de niños, niñas y adolescentes, y en ese sentido está comprometida con
							el respeto y garantía de los derechos de los Titulares de los datos que recolecta y trata en
							su calidad de Responsable del tratamiento, cumpliendo con la normatividad vigente
							adoptando las presentes Políticas, las cuales son de obligatoria aplicación en todas las
							actividades que involucren total o parcialmente la recolección, el almacenamiento, el uso y
						la circulación de esa información.</p>
						<p class="text-justify">Por lo anterior, tanto JUNICAL MEDICAL S.A.S como todos los terceros y todos los sujetos
							involucrados en el tratamiento de datos personales, deben observar y respetar las normas
							en materia de protección de datos personales, la confidencialidad de los datos tratados y
							esta política en el cumplimiento de sus funciones y/o actividades aún después de
							terminados los vínculos legales, comerciales, laborales o de cualquier índole con JUNICAL
						MEDICAL S.A.S.</p>
						<p class="text-justify">La presente Política se encuentra publicadas en la página web de JUNICAL MEDICAL
							S.A.S (www.junicalmedical.com.co), con el fin de que sean conocidas por los Titulares de
						los datos y los terceros que tengan acceso a los datos personales administrados por ésta.</p>
